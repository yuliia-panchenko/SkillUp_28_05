print('Здравствуйте, ', user_name, ' ', user_surname, '!', sep='')
print()

# ================Task_3==============

meters = float(input('Введите количество метров: '))
print(meters, 'метров равняется', meters / 1000, 'километров и', meters * 1000, 'миллиметров')